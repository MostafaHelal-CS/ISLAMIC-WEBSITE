let isPlaying=false;
function playControl () {
    if(isPlaying) {
        audioControl.pause();
        playButton.innerHTML=`<i class="fa-solid fa-play"></i>`;
        isPlaying=false;
    } else {
        audioControl.play();
        playButton.innerHTML=`<i class="fa-solid fa-pause"></i>`;
        isPlaying=true;
    }
}


function displayReciters (reciters) {
    const recitersContainer=document.querySelector('.reciters');
    const surahsContainer=document.getElementById('surah-container');
    const nextButton=document.getElementById('next');
    const playButton=document.getElementById('play');
    const prevButton=document.getElementById('prev');
    const audioControl=document.getElementById('audioControl'); // Assuming you have an audio element with this ID

    let activeCard=null; // Track the currently active card
    let currentSurahIndex=-1; // Track the current surah index
    let currentSurahItems=[]; // Store the current surah DOM elements

    reciters.forEach(reciter => {
        // Create card element
        const card=document.createElement('div');
        card.classList.add('reciter-card');

        // Create and append image
        const img=document.createElement('img');
        img.src=reciter.image;
        img.alt=reciter.name;
        img.classList.add('reciter-name');
        img.id=reciter.id;
        card.appendChild(img);

        // Create and append Arabic name
        const nameArabic=document.createElement('p');
        nameArabic.classList.add('reciter-name');
        nameArabic.textContent=reciter.nameArabic;
        nameArabic.style.cssText="color: #fff; padding-top: 10px; font-weight: bold;";
        card.appendChild(nameArabic);

        // Append card to container
        recitersContainer.appendChild(card);

        // Create list container for surahs
        const list=document.createElement('div');
        list.className="surahlist";

        card.addEventListener('click', () => {
            // Clear previous surahs and remove active class from previously active card
            if(activeCard) {
                activeCard.classList.remove('active');
                while(surahsContainer.firstChild) {
                    surahsContainer.removeChild(surahsContainer.firstChild);
                }
            }

            console.log(reciter.id, reciter.nameArabic, reciter.server);
            card.classList.add('active');
            activeCard=card; // Update the active card

            currentSurahItems=[];
            currentSurahIndex=-1;

            const detectSurahs=reciter.surahs;
            detectSurahs.forEach((surah, index) => {
                // Create surah container
                const listItem=document.createElement('div');
                listItem.id=surah.id;
                listItem.className=surah.name;
                listItem.classList.add('surah');

                // Create and append surah name and id
                const surahName=document.createElement('span');
                surahName.innerText=surah.name;
                const surahId=document.createElement('span');
                surahId.innerText=surah.id;
                listItem.append(surahName, surahId);

                list.append(listItem);
                currentSurahItems.push(listItem); // Store the surah item

                // Add click event to play surah and manage active state
                listItem.addEventListener('click', () => {
                    // Remove active class from all surah items
                    currentSurahItems.forEach(item => {
                        item.classList.remove('active');
                    });

                    // Add active class to the clicked surah item
                    listItem.classList.add('active');
                    audioControl.src=reciter.server+`${surah.id}.mp3`;
                    audioControl.play();
                    currentSurahIndex=index;
                });
            });

            surahsContainer.append(list);
        });
    });

    function playSurah (index) {
        if(index>=0&&index<currentSurahItems.length) {
            // Remove active class from all surah items
            currentSurahItems.forEach(item => {
                item.classList.remove('active');
            });

            // Add active class to the current surah item
            const listItem=currentSurahItems[index];
            listItem.classList.add('active');
            const surahId=listItem.id;
            const reciter=reciters.find(r => r.surahs.some(s => s.id===surahId)); // Find the reciter
            audioControl.src=reciter.server+`${surahId}.mp3`;
            audioControl.play(); // Play the surah
        }
    }

    nextButton.addEventListener('click', () => {
        if(currentSurahIndex<currentSurahItems.length-1) {
            currentSurahIndex++;
            playSurah(currentSurahIndex);
        }
    });

    prevButton.addEventListener('click', () => {
        if(currentSurahIndex>0) {
            currentSurahIndex--;
            playSurah(currentSurahIndex);
        }
    });

    playButton.addEventListener('click', () => {
        if(currentSurahIndex>=0&&currentSurahIndex<currentSurahItems.length) {
            playControl();
        }
    });
}

// Call the function to display reciters
displayReciters(reciters);



